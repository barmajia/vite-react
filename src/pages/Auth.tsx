// ── Auth Callback Page ───────────────────────────────────────────────

export const AuthCallbackPage: React.FC = () => {
  const navigate = useNavigate();
  const [searchParams] = useSearchParams();
  const { user } = useAuth();
  const { seller, loading } = useSeller();

  useEffect(() => {
    // Handle OAuth callback
    const code = searchParams.get("code");
    const type = searchParams.get("type");

    if (type === "signup" && user) {
      // New user from OAuth - redirect to template selection
      navigate("/onboarding/template-selection", { replace: true });
    } else if (user && seller) {
      // Existing user - redirect to dashboard
      if (seller.current_template_id) {
        navigate("/dashboard", { replace: true });
      } else {
        navigate("/onboarding/template-selection", { replace: true });
      }
    }
  }, [user, seller, searchParams, navigate]);

  return (
    <div className="min-h-screen flex items-center justify-center bg-gradient-to-br from-blue-50 to-indigo-100">
      <div className="text-center">
        <div className="animate-spin rounded-full h-16 w-16 border-b-2 border-blue-600 mx-auto mb-4"></div>
        <p className="text-gray-600 text-lg">Completing sign in...</p>
      </div>
    </div>
  );
};

// ── Protected Route Wrapper ──────────────────────────────────────────

interface ProtectedRouteProps {
  children: React.ReactNode;
  requireOnboarding?: boolean;
}

export const ProtectedRoute: React.FC<ProtectedRouteProps> = ({
  children,
  requireOnboarding = false,
}) => {
  const navigate = useNavigate();
  const { user, loading: authLoading } = useAuth();
  const { seller, loading: sellerLoading } = useSeller();

  useEffect(() => {
    if (!authLoading && !user) {
      navigate("/login", { replace: true });
      return;
    }

    if (!sellerLoading && user && seller && requireOnboarding) {
      // If user has already completed onboarding, redirect to dashboard
      if (seller.current_template_id) {
        navigate("/dashboard", { replace: true });
      }
    }
  }, [user, seller, authLoading, sellerLoading, navigate, requireOnboarding]);

  if (authLoading || sellerLoading) {
    return (
      <div className="min-h-screen flex items-center justify-center bg-gradient-to-br from-blue-50 to-indigo-100">
        <div className="text-center">
          <div className="animate-spin rounded-full h-16 w-16 border-b-2 border-blue-600 mx-auto mb-4"></div>
          <p className="text-gray-600 text-lg">Loading...</p>
        </div>
      </div>
    );
  }

  if (!user) {
    return null;
  }

  return <>{children}</>;
};

export default SignupPage;
